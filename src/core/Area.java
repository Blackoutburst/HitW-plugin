package core;

public class Area {
	public float x0;
	public float x1;
	public float y0;
	public float y1;
	public float z0;
	public float z1;
	
	public Area(float x0, float y0, float z0, float x1, float y1, float z1) {
		this.x0 = x0;
		this.y0 = y0;
		this.z0 = z0;
		this.x1 = x1;
		this.y1 = y1;
		this.z1 = z1;
	}
	
}
